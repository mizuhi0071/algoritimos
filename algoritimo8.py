print("Bem-vindo ao calculador de salario mensal")
valor = float(input("Qual o valor recebido por horas trabalhadas"))
horas = float(input("Quantas horas foram trabalhadas?"))
resultado = valor * horas
print(f"Foram trabalhadas {horas} horas, e o valor por hora é de R$ {valor}. O valor do salario mensal é de R$ {resultado}!!")